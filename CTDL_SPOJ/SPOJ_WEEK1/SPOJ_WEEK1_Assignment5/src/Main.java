public class Main {

    public static void main(String[] args) {
        InputReader reader = new InputReader(System.in);
        int numProduct = reader.nextInt();
        long money = reader.nextLong();
        long[] productValues = new long[numProduct];
        for (int i = 0; i < numProduct; i++) {
            productValues[i] = reader.nextLong();
        }
        Arrays.sort(productValues);
        int j;
        for (j = productValues.length - 1; j >= 0; j--) {
            if (productValues[j] <= money) {
                System.out.println(productValues[j]);
                break;
            }
        }

        if (j < 0) {
            System.out.println("-1");
        }

    }

    static class InputReader {

        StringTokenizer tokenizer;
        BufferedReader reader;
        String token;
        String temp;

        public InputReader(InputStream stream) {
            tokenizer = null;
            reader = new BufferedReader(new InputStreamReader(stream));
        }

        public InputReader(FileInputStream stream) {
            tokenizer = null;
            reader = new BufferedReader(new InputStreamReader(stream));
        }

        public String nextLine() throws IOException {
            return reader.readLine();
        }

        public String next() {
            while (tokenizer == null || !tokenizer.hasMoreTokens()) {
                try {
                    if (temp != null) {
                        tokenizer = new StringTokenizer(temp);
                        temp = null;
                    } else {
                        tokenizer = new StringTokenizer(reader.readLine());
                    }

                } catch (IOException e) {
                }
            }
            return tokenizer.nextToken();
        }

        public double nextDouble() {
            return Double.parseDouble(next());
        }

        public int nextInt() {
            return Integer.parseInt(next());
        }

        public long nextLong() {
            return Long.parseLong(next());
        }
    }

}
